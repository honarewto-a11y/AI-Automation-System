module.exports = {
    generate(input) {
        const q = (input || "").toString().trim().toLowerCase();

        // تاکتیک برای پول درآوردن
        if (q.includes("پول") && (q.includes("چطور") || q.includes("چگونه"))) {
            return [
                "۱) امروز یک مهارت انتخاب کن.",
                "۲) همین امروز یک نمونه‌کار کوچک بساز.",
                "۳) آن را در یک کانال یا گروه منتشر کن.",
                "۴) به ۵ نفر پیام بده و پیشنهاد بده.",
                "۵) اولین مشتری را حتی با قیمت کم بگیر.",
                "۶) خروجی را سریع تحویل بده.",
                "۷) درآمد را ثبت کن و فردا تکرار کن."
            ].join("\n");
        }

        // تاکتیک برای مهاجرت
        if (q.includes("مهاجرت")) {
            return [
                "۱) هدف مهاجرت را مشخص کن (کار/تحصیل).",
                "۲) کشور مناسب را انتخاب کن.",
                "۳) زبان را شروع کن (روزانه ۳۰ دقیقه).",
                "۴) رزومه را استاندارد کن.",
                "۵) ۱۰ شرکت یا دانشگاه را لیست کن.",
                "۶) امروز ۲ درخواست ارسال کن.",
                "۷) هر هفته ۱۰ درخواست جدید."
            ].join("\n");
        }

        // تاکتیک برای برنامه‌نویس شدن
        if (q.includes("برنامه") && q.includes("نویس")) {
            return [
                "۱) امروز HTML را شروع کن (۱ ساعت).",
                "۲) فردا CSS را شروع کن.",
                "۳) روز سوم JavaScript.",
                "۴) یک پروژه کوچک بساز (ToDo List).",
                "۵) آن را در گیت‌هاب منتشر کن.",
                "۶) هر هفته یک پروژه جدید.",
                "۷) بعد از ۶ هفته → فریم‌ورک."
            ].join("\n");
        }

        return null;
    },

    apply(answer, context) {
        const t = this.generate(context.input);
        if (!t) return answer;

        return (
            answer +
            "\n\n⚡ تاکتیک‌های عملیاتی:\n" +
            t
        );
    }
};
