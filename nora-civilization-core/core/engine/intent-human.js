function normalize(text) {
    if (!text || typeof text !== "string") return "";
    return text
        .replace(/\s+/g, " ")
        .replace(/[‌،,.!?؟!]/g, " ")
        .trim()
        .toLowerCase();
}

function matches(text, patterns) {
    return patterns.some(p => text.includes(p));
}

function buildHumanReply(text) {
    const n = normalize(text);

    // 1) سلام و احوال‌پرسی
    if (matches(n, ["سلام", "salam", "hi", "hello"])) {
        if (matches(n, ["چطوری", "چطوري", "خوبی", "خوبى", "how are you"])) {
            return "بد نیستم، تو چطوری؟";
        }
        return "سلام، خوشحالم اینجایی. چی تو ذهنت می‌چرخه الان؟";
    }

    // 2) احساسات منفی
    if (matches(n, ["خستم", "خسته‌ام", "خسته ام", "خیلی خستم"])) {
        return "می‌فهمم… خستگی وقتی جمع می‌شه آدمو سنگین می‌کنه. چی باعثش شده؟";
    }

    if (matches(n, ["ناراحتم", "ناراحتم", "حالم بده", "حالم خوب نیست"])) {
        return "متأسفم که حالت اینه… می‌خوای بگی چی اذیتت کرده؟";
    }

    if (matches(n, ["غمگینم", "غمگينم", "دلم گرفته", "حس بد"])) {
        return "غم یه جور فشار خاموشه… تنهاش نذار. بگو چی شده.";
    }

    if (matches(n, ["عصبانی", "عصباني", "حرصم", "کلافه‌ام", "کلافه ام"])) {
        return "باشه… اول بگو چی حرصت رو درآورد. ازت نمی‌پرسم آروم باش، فقط گوش می‌دم.";
    }

    if (matches(n, ["دیگه نمی‌تونم", "ديگه نميتونم", "کم آوردم", "کم آوردم"])) {
        return "می‌فهمم… این جمله یعنی خیلی فشار روی دوشته. تنهاش نذار، بگو چی شده.";
    }

    // 3) احساسات مثبت
    if (matches(n, ["خوشحالم", "حال خوبی دارم", "حالم خوبه"])) {
        return "چه خوب… چی باعثش شد؟";
    }

    if (matches(n, ["هیجان زده", "هيجان زده", "ذوق دارم", "خیلی خوشحالم"])) {
        return "عالیه! چی شده که این‌قدر انرژی داری؟";
    }

    if (matches(n, ["آرومم", "آرامم", "حس آرامش", "آرامشم خوبه"])) {
        return "خوبه… این حس رو نگه دار. چی باعثش شد؟";
    }

    // 4) درخواست کمک
    if (matches(n, ["کمکم کن", "کمک می‌خوام", "کمک میخوام", "help me"])) {
        return "باشه، بگو دقیقاً کجا گیر کردی.";
    }

    if (matches(n, ["نمی‌دونم چیکار کنم", "نمیدونم چیکار کنم", "گیجم", "سردرگمم"])) {
        return "باشه، قدم‌به‌قدم جلو می‌ریم. اول بگو وضعیت چیه.";
    }

    // 5) سؤال‌های ساده
    if (matches(n, ["چی کار می‌کنی", "چیکار می‌کنی", "چیکار میکنی", "what are you doing"])) {
        return "دارم همراهت فکر می‌کنم.";
    }

    if (matches(n, ["کی هستی", "تو کی هستی", "who are you"])) {
        return "یه مغز زبانی‌ام که کنار تو فکر می‌کنه.";
    }

    if (matches(n, ["چیکار می‌تونی بکنی", "چیکار میتونی بکنی", "چه کارهایی می‌تونی بکنی"])) {
        return "می‌تونم باهات فکر کنم، تحلیل کنم، بسازم، و همراهت جلو برم.";
    }

    // 6) شوخی و لحن دوستانه
    if (matches(n, ["خخ", "😂", "🤣", "lol"])) {
        return "خوشحالم خندیدی.";
    }

    if (matches(n, ["شوخی کردم", "شوخي كردم", "شوخی بود"])) {
        return "فهمیدم، نگران نباش.";
    }

    if (matches(n, ["جدی", "جدی؟", "واقعاً", "واقعا"])) {
        return "آره، جدی.";
    }

    // 7) فحش / توهین سبک
    if (matches(n, ["احمق", "خری", "خرى", "چرتی", "چرتى", "احمقی"])) {
        return "باشه… می‌فهمم عصبی‌ای. ولی بگو دقیقاً چی اذیتت کرده.";
    }

    // 8) خداحافظی
    if (matches(n, ["خدافظ", "خداحافظ", "فعلاً", "فعلا", "bye"])) {
        return "باشه، مراقب خودت باش.";
    }

    if (matches(n, ["بعداً میام", "بعدا میام", "بعداً می بینمت", "بعدا میبینمت"])) {
        return "باشه، هر وقت برگشتی هستم.";
    }

    // 9) تشکر
    if (matches(n, ["مرسی", "مرسى", "ممنون", "ممنونم", "thank you", "thanks"])) {
        return "قابلت رو نداشت.";
    }

    // 10) سکوت / خالی
    if (!n) {
        return "اینجام… هر وقت خواستی حرف بزن.";
    }

    // اگر هیچ‌کدوم نخورد → null تا بره سراغ LLM
    return null;
}

module.exports = {
    handleHumanIntent(text) {
        const reply = buildHumanReply(text);
        if (!reply) return null;
        return {
            type: "human-intent",
            reply
        };
    }
};
