module.exports = {
    answer(text) {
        const q = (text || "").toString().trim().toLowerCase();

        // 1) پول درآوردن
        if (q.includes("پول") && (q.includes("چطور") || q.includes("چگونه"))) {
            return [
                "۱) یک مهارت قابل فروش انتخاب کن (طراحی، برنامه‌نویسی، تولید محتوا، فروش، تعمیرات).",
                "۲) نیاز بازار را پیدا کن (کجا برای این مهارت پول می‌دهند؟).",
                "۳) نمونه‌کار بساز حتی اگر رایگان باشد.",
                "۴) اولین مشتری را سریع پیدا کن (تلگرام، اینستاگرام، سایت‌های فریلنسری).",
                "۵) درآمد را مقیاس بده: افزایش قیمت، افزایش سرعت، افزایش کیفیت.",
                "۶) یک برند کوچک شخصی بساز.",
                "۷) استمرار = پول واقعی."
            ].join("\n");
        }

        // 2) ساخت سایت
        if (q.includes("سایت") && (q.includes("چطور") || q.includes("چگونه"))) {
            return [
                "مسیر ساخت سایت:",
                "۱) انتخاب روش: وردپرس / سایت‌ساز / کدنویسی.",
                "۲) انتخاب دامنه + هاست.",
                "۳) نصب قالب یا طراحی UI.",
                "۴) ساخت صفحات اصلی: خانه، درباره، تماس.",
                "۵) اتصال به درگاه پرداخت یا فرم‌ها.",
                "۶) سئو پایه برای دیده‌شدن.",
                "۷) انتشار و نگهداری."
            ].join("\n");
        }

        // 3) مهاجرت
        if (q.includes("مهاجرت")) {
            return [
                "برای مهاجرت باید سه چیز مشخص شود:",
                "۱) هدف: کار / تحصیل / زندگی / سرمایه‌گذاری.",
                "۲) مسیر: زبان / مدرک / سابقه کار / تمکن مالی.",
                "۳) کشور مناسب: بر اساس هدف و شرایط.",
                "قانون طلایی: مهاجرت = پروژه ۱۲ تا ۲۴ ماهه."
            ].join("\n");
        }

        // 4) موفقیت
        if (q.includes("موفق") || q.includes("موفقیت")) {
            return [
                "موفقیت = سه چیز:",
                "۱) مهارت عمیق.",
                "۲) استمرار بدون وقفه.",
                "۳) تصمیم‌گیری درست در لحظه‌های مهم.",
                "قانون تمدنی: موفقیت نتیجهٔ رفتارهای کوچک روزانه است."
            ].join("\n");
        }

        // 5) برنامه‌نویس شدن
        if (q.includes("برنامه") && q.includes("نویس")) {
            return [
                "مسیر برنامه‌نویس شدن:",
                "۱) HTML + CSS",
                "۲) JavaScript",
                "۳) یک فریم‌ورک: React یا Vue",
                "۴) بک‌اند: Node.js",
                "۵) دیتابیس: MongoDB یا PostgreSQL",
                "۶) ساخت ۳ پروژه واقعی",
                "۷) رزومه + گیت‌هاب + نمونه‌کار",
                "قانون تمدنی: پروژه مهم‌تر از دوره است."
            ].join("\n");
        }

        return null;
    }
};
