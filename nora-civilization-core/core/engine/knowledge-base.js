module.exports = {
    answer(text) {
        const q = (text || "").toString().trim().toLowerCase();

        if (q.includes("چرا آسمان آبی است")) {
            return "نور خورشید هنگام عبور از جو زمین پراکنده می‌شود. طول موج‌های آبی بیشتر پخش می‌شوند، برای همین آسمان آبی دیده می‌شود.";
        }

        if (q.includes("چطور پول دربیارم") || q.includes("چگونه پول دربیارم")) {
            return "برای پول درآوردن باید یک مهارت قابل فروش انتخاب کنی، نیاز بازار را پیدا کنی و مداوم ارائه‌اش دهی.";
        }

        if (q.includes("چطور سایت بسازم") || q.includes("چگونه سایت بسازم")) {
            return "برای ساخت سایت می‌توانی از وردپرس، سایت‌سازها یا کدنویسی مستقیم استفاده کنی. انتخاب مسیر به مهارت و زمان تو بستگی دارد.";
        }

        if (q.includes("هوش مصنوعی چیست") || q.includes("هوش مصنوعی چیه")) {
            return "هوش مصنوعی سیستمی است که می‌تواند یاد بگیرد، تصمیم بگیرد و الگوها را تشخیص دهد، مشابه انسان.";
        }

        return null;
    }
};
